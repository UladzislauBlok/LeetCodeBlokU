package org.bloku.task._3021;

import org.bloku.util.LeetCodeName;
import org.bloku.util.Topic;
import org.bloku.util.Topics;

@LeetCodeName("Alice and Bob Playing Flower Game")
@Topics({Topic.MATH})
class Solution {

    public long flowerGame(int n, int m) {
        return ((long) n * m) / 2;
    }
}
